'use client'

import { useEffect, useRef } from 'react'
import { useTheme } from 'next-themes'

const SPACING = 7
const THICKNESS = Math.pow(37, 2.5)
const DRAG = 0.95
const EASE = 0.1

interface Particle {
  vx: number
  vy: number
  x: number
  y: number
  ox: number
  oy: number
  show: boolean
}

function HeaderEffect() {
  const { theme } = useTheme()
  const containerRef = useRef<HTMLDivElement>(null)
  const canvasRef = useRef<HTMLCanvasElement>(null)
  const animationFrameId = useRef<number>(null)
  const themeRef = useRef(theme)
  let list: Particle[] = []
  let w: number, h: number
  let ctx: CanvasRenderingContext2D | null
  let mx = -500,
    my = -500
  let tog = false

  const handleMouseMove = (e: MouseEvent) => {
    if (window.innerWidth < 700) return
    const bounds = canvasRef.current?.getBoundingClientRect()
    if (bounds) {
      mx = e.clientX - bounds.left
      my = e.clientY - bounds.top
    }
  }

  const init = () => {
    if (containerRef.current && canvasRef.current) {
      const container = containerRef.current
      const canvas = canvasRef.current

      const ow = container.offsetWidth
      const oh = container.offsetHeight

      w = Math.ceil(ow * 4)
      h = Math.ceil(oh * 4)

      canvas.width = w
      canvas.height = h

      const ws = Math.ceil(w / 4)
      const hs = Math.ceil(h / 4)

      let i = 0
      list = []
      for (let x = ws; x < ws + ow; x += SPACING) {
        for (let y = hs; y < hs + oh; y += SPACING) {
          const p: Particle = {
            vx: 0,
            vy: 0,
            x,
            y,
            ox: x,
            oy: y,
            show: data[i],
          }
          list[i] = p
          i++
        }
      }
    }
  }

  const step = () => {
    if (!ctx) return

    let p: Particle, d, dx, dy, f, t, a, b, n

    if ((tog = !tog)) {
      for (let i = 0; i < list.length; i++) {
        p = list[i]
        if (!p.show) continue
        d = (dx = mx - p.x) * dx + (dy = my - p.y) * dy
        f = -THICKNESS / d
        if (d < THICKNESS) {
          t = Math.atan2(dy, dx)
          p.vx += f * Math.cos(t)
          p.vy += f * Math.sin(t)
        }
        p.x += (p.vx *= DRAG) + (p.ox! - p.x) * EASE
        p.y += (p.vy *= DRAG) + (p.oy! - p.y) * EASE

        if (Math.abs(p.x - p.ox) < 0.5 && Math.abs(p.y - p.oy) < 0.5) {
          p.x = p.ox
          p.y = p.oy
          p.vx = 0
          p.vy = 0
        }
      }

      const b = (a = ctx.createImageData(w, h)).data
      for (let i = 0; i < list.length; i++) {
        p = list[i]
        if (!p.show) continue
        n = (~~p.x + ~~p.y * w) * 4
        b[n] = b[n + 1] = b[n + 2] = themeRef.current === 'light' ? 0 : 224
        b[n + 3] = 255
      }
      ctx.putImageData(a, 0, 0)
    }

    animationFrameId.current = requestAnimationFrame(step)
  }

  function restartParticles() {
    if (ctx) {
      ctx.clearRect(0, 0, w, h)
      init()
    }
  }

  useEffect(() => {
    themeRef.current = theme
    restartParticles();
  }, [theme])

  useEffect(() => {
    if (canvasRef.current) {
      ctx = canvasRef.current.getContext('2d')
    }
  }, [containerRef.current, canvasRef.current])

  useEffect(() => {
    init()
    step()
    window.addEventListener('resize', restartParticles)
    window.addEventListener('mousemove', handleMouseMove)
    return () => {
      if (animationFrameId.current) cancelAnimationFrame(animationFrameId.current)
      window.removeEventListener('resize', restartParticles)
      window.removeEventListener('mousemove', handleMouseMove)
    }
  }, []);

  return (
    <div style={{ width: 280, height: 280 }} ref={containerRef}>
      <canvas
        className="pointer-events-none z-0"
        style={{ transform: 'translate(-25%,-25%)' }}
        ref={canvasRef}
      />
    </div>
  )
}

export default HeaderEffect

